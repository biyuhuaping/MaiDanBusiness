#import "TTButton.h"

@implementation TTButton

- (instancetype)initWithFrame:(CGRect)frame
{
    self = [super initWithFrame:frame];
    
    if (self)
    {
        //标题1
        _lbl1      = [[UILabel alloc] initWithFrame:CGRectMake(0, 5, G_SCREEN_WIDTH/2, 20)];
        _lbl1.font          = [UIFont systemFontOfSize:13];
        _lbl1.textColor     = [UIColor blackColor];
        _lbl1.textAlignment = NSTextAlignmentCenter;
        [self addSubview:_lbl1];
        
        //标题2
        _lbl2      = [[UILabel alloc] initWithFrame:CGRectMake(0, 25, G_SCREEN_WIDTH/2, 20)];
        _lbl2.font          = [UIFont systemFontOfSize:13];
        _lbl2.textColor     = [UIColor blackColor];
        _lbl2.textAlignment = NSTextAlignmentCenter;
        [self addSubview:_lbl2];
    }
    
    return self;
}

@end
